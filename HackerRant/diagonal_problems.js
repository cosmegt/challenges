function diagonalDifference(a) {
    var x = 0;
    var y = 0;
    for (var i in a){
        x += a[i][i]
        y += a[i][a.length-i-1]
        //console.log(x += a[i][i]);
        //console.log(y += a[i][a.length-i-1])
    }

    var z = (y - x);
    if(z < 0){
        z = z*-1;
    }
    console.log(z)
    return z;
}
